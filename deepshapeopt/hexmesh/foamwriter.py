def checkmesh_log_ok(log_text: str) -> tuple[bool, list[str]]:
    """Parse a checkMesh log; returns (ok, failed check descriptions)."""
    failed: list[str] = []
    for line in log_text.splitlines():
        stripped = line.strip()
        if stripped.startswith("***"):
            failed.append(stripped.lstrip("*").strip())
    m = re.search(r"Failed (\d+) mesh checks", log_text)
    if m and int(m.group(1)) > 0:
        return False, failed
    if "Mesh OK." in log_text:
        return True, failed
    # No verdict found: treat as failure so problems never pass silently.
    return False, ["checkMesh log contains no verdict (crashed?)"]
